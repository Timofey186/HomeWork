number = int(input('Введите число'))
number1 = int(input('Введите второе число'))
number2 = int(input('Введите третье число'))
if number == number1 :
    print(2)
elif number == number2 :
    print(2)
elif number1 == number2 :
    print(2)
elif number == number1 == number2 :
    print(3)
else:
    print(0)
